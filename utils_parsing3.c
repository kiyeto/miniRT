#include "header.h"

int		valid_n(t_v3 *p)
{
	if (p->x == 0.069 || p->y == 0.069 || p->z == 0.069
		|| !(p->x >= -1 && p->x <= 1) || !(p->y >= -1 && p->y <= 1)
		|| !(p->z >= -1 && p->z <= 1))
		return (0);
	return (1);
}
